package com.ia.core.forms.impl;

public final class ContentTypeException extends Exception {

    private static final long serialVersionUID = 3937474093209851208L;

    public ContentTypeException() {
        super();
    }

    public ContentTypeException(final String message) {
        super(message);
    }

    public ContentTypeException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public ContentTypeException(final Throwable cause) {
        super(cause);
    }

}
